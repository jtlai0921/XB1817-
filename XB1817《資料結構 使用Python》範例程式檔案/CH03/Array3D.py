#產生[2][2][3]三維陣列
number = [[[11, 12, 13], [21, 22, 3]],
   [[31, 33, 35], [42, 44, 46]]]

#回傳第1個二維陣列 - [[11, 12, 13], [21, 22, 3]]
print(number[0])

#回傳第2個二維陣列 - [[31, 33, 35], [42, 44, 46]]
print(number[1])

#回傳第0個二維陣列第0列項目 - [11, 12, 13]
print('第0個二維陣列', number[0][0])

#回傳第0個二維陣列第0列、第0欄項目 - [11]
print('第0個二維陣列', number[0][0][0])

print('第0個二維陣列', number[1][1][2])
